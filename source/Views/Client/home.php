<script type="text/javascript">
    (new Splide("#splide-testimonials", {
        rewind: true,
        rewindSpeed: 1000,
        perPage: 2,
        gap: "2rem",
        breakpoints: {
            900: {
                perPage: 1,
            },
        }
    })).mount();

    const posts = document.getElementById("posts");
    const paginator = document.getElementById("paginator");

    loadPage(<?= $page ?>);
    function loadPage(page) {
        let pageInformed = true;
        if(!page) {
            page = 1;
            pageInformed = false;
        }

        const origin = window.location.origin;
        const link = origin + "/api/posts/pegar/" + page;
        
        fetch(link, {method: "GET"})
            .then(function(response) {
                return response.json();
            })
            .then(function(data) {
                // EVITA QUE A URL SEJA MUDADA NO LOADING DA PÁGINA
                if(pageInformed) {
                    window.history.pushState({}, "Página de Posts", page);
                }

                // CONFIGURA O PAGINATOR
                paginator.innerHTML = data.paginator;
                const paginatorLinks = paginator.querySelectorAll("a");
                paginatorLinks.forEach(item => {
                    item.addEventListener("click", (e) => {
                        e.preventDefault();

                        const href = e.target.href;
                        const page = href.slice(href.lastIndexOf("/") + 1);
                        loadPage(page);
                    })
                });

                // RENDERIZA OS POSTS
                posts.innerHTML = "";
                data.posts.forEach((item) => {
                    const li = document.createElement("li");
                    const figure = document.createElement("figure");
                    const img = document.createElement("img");
                    img.src = origin + `/assets/img/post/${item.id}/cape.png`
                    img.alt = item.title;
                    figure.appendChild(img);
                    const p = document.createElement("p");
                    if (item.posts){
                        p.innerHTML = `<a class="link" href="posts/${item.title_url}">${item.title}</a>`;
                    }else {
                        p.textContent = item.title;
                    }

                    li.appendChild(figure);
                    li.appendChild(p);
                    posts.appendChild(li);
                })
            })
            .catch(function(err) {
                console.error(err);
            });
    }
</script>
